# ---------------- PROCESS IMAGE ----------------
@app.route('/process_image', methods=['POST'])
def process_image():

    try:
        # ✅ check image exists
        if 'image' not in request.files:
            return jsonify({
                "status": "error",
                "message": "No image uploaded"
            }), 400

        file = request.files['image']

        # convert image
        npimg = np.frombuffer(file.read(), np.uint8)
        img = cv2.imdecode(npimg, cv2.IMREAD_COLOR)

        # detect hands
        hands, img = detector.findHands(img)

        if not hands:
            return jsonify({
                "status": "no_hand",
                "letter": "No Hand Detected"
            })

        left_letter = None
        right_letter = None

        for hand in hands:
            fingers = tuple(detector.fingersUp(hand))
            hand_type = hand["type"].lower()

            letter = uyir_mei_gestures.get(hand_type, {}).get(fingers, None)

            if hand_type == "left":
                left_letter = letter
            else:
                right_letter = letter

        # combine logic
        result = "Unknown"

        if left_letter and right_letter:
            result = uyir_mei_map.get((left_letter, right_letter), "Unknown")
        elif right_letter:
            result = right_letter
        elif left_letter:
            result = left_letter

        return jsonify({
            "status": "success",
            "letter": result
        })

    except Exception as e:
        return jsonify({
            "status": "error",
            "message": str(e)
        }), 500


